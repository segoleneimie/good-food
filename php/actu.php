            <h1>Pédagogique d’appropriation concrète par tous</h1>

            <p class="texte">L’atelier-cuisine est un outil pédagogique irremplaçable, de par son
            caractère très concret : chacun participe, « met la main à la pâte », et s’approprie les idées.
            <br/>Cuisiner est une expérience avant tout sensorielle ! L’ensemble des sens sont mobilisés pour la découverte et le plaisir : le goût et l’odorat bien sûr,mais aussi le toucher et la vue avec les gestes, et l’ouïe dans la cuisson ou la reconnaissance des textures ! Cuisiner est donc accessible à tous… En faisant appel à des compétences peu souvent
            ou insuffisamment valorisées.
            <br/> C’est à travers la mise en pratique, la découverte concrète des produits et
            des démarches que le citoyen peut progressivement se réapproprier son alimentation. En cuisinant,
            le consommateur augmente ses possibilités de choix et améliore la maîtrise de son alimentation.</p>

            <p class="texte">Quant aux enfants, ils découvrent une nouvelle porte d’entrée pour s’intéresser à l’alimentation. L’atelier cuisine, c’est aussi un grand moment de convivialité, un espace de rencontres et d’échanges autour d’un sujet qui passionne tout le monde, des plus petits aux plus âgés.<br/> Il offre l’opportunité à chacun de participer à la construction d’une oeuvre collective : le repas ou les mets qui seront ensuite dégustés tous ensemble.
            <br/> Il contribue ainsi à retrouver confiance en soi et à restaurer l’estime de soi, primordiale pour une bonne alimentation, et qui peut faire défaut, notamment aux populations les plus démunies.</p>


